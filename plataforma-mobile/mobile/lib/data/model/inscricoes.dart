import 'package:sqflite/sqflite.dart';

Future<void> createInscricoesTable(Database db) async {
  await db.execute('''
    CREATE TABLE IF NOT EXISTS INSCRICOES (
      ID_INSCRICAO INTEGER PRIMARY KEY AUTOINCREMENT,
      ID_FORMANDO INTEGER NOT NULL,
      ID_CURSO INTEGER NOT NULL,
      DATA_INSCRICAO TEXT NOT NULL DEFAULT CURRENT_TIMESTAMP,
      STATUS_INSCRICAO INTEGER NOT NULL DEFAULT 1,
      FOREIGN KEY (ID_CURSO) REFERENCES CURSOS(ID_CURSO),
      FOREIGN KEY (ID_FORMANDO) REFERENCES FORMANDOS(ID_FORMANDO)
    );
  ''');
}