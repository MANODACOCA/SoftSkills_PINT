import 'package:sqflite/sqflite.dart';

Future<void> createMaterialApoioTable(Database db) async {
  await db.execute('''
    CREATE TABLE IF NOT EXISTS MATERIAL_APOIO (
      ID_MATERIAL_APOIO INTEGER PRIMARY KEY AUTOINCREMENT,
      ID_CURSO INTEGER NOT NULL,
      ID_FORMATO INTEGER NOT NULL,
      CONTEUDO TEXT NOT NULL,
      NOME_MATERIAL TEXT NOT NULL,
      FOREIGN KEY (ID_FORMATO) REFERENCES TIPO_FORMATO(ID_FORMATO),
      FOREIGN KEY (ID_CURSO) REFERENCES CURSOS(ID_CURSO)
    );
  ''');
}
